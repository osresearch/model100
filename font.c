static void
lcd_select(
	uint8_t x,
	uint8_t y,
	uint8_t val
)
{
	if (y < 32)
	{
		// Top half of the display
		if (x < 50)
			out(LCD_CS20, val);
		else
		if (x < 100)
			out(LCD_CS21, val);
		else
		if (x < 150)
			out(LCD_CS22, val);
		else
		if (x < 200)
			out(LCD_CS23, val);
		else
			out(LCD_CS24, val);
	} else {
		// Bottom half of the display
		if (x < 50)
			out(LCD_CS25, val);
		else
		if (x < 100)
			out(LCD_CS26, val);
		else
		if (x < 150)
			out(LCD_CS27, val);
		else
		if (x < 200)
			out(LCD_CS28, val);
		else
			out(LCD_CS29, val);
	}
}


void
lcd_char(
	uint8_t col,
	uint8_t row,
	uint8_t c
)
{
	uint8_t x = col * 6;
	uint8_t y = row * 8;

	const char * f = font[c];

	for (uint8_t i = 0 ; i < 6 ; i++, x++)
	{
		// this would be faster if we used the auto-increment
		// functions, but has lots of special cases for crossing
		// display boundaries.
		lcd_display(x, y, pgm_read_byte(&f[i]));
	}
}
